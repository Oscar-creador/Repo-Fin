    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        try(Connection connection = App.getConnection()){
            PrestamoDbDao pdbdao = App.getPrestamosDao(connection);
            Alumno a = new Alumno();
            AlumnoDbDao adbdao = App.getAlumnosDao(connection);
            a = adbdao.getById(Integer.parseInt(this.txt_matricula.getText()));
            if(a == null)return;
            LibroInventario l = new LibroInventario();
            LibroInventarioDbDao lidbdao = App.getLibrosInventarioDao(connection);
            l = lidbdao.getById(Integer.parseInt(this.txt_idLibroEnInventario.getText()));
            if(l == null)return;
            if(l.getEstado()=="false")return;
            p = new Prestamo();
            p.setAlumnoId(a.getId());
            p.setLibroInventarioId(l.getId());
            p.setFechaDebeDevolver(LocalDateTime.ofInstant(DateUtils.parseDate(this.txt_fecha.getText()).toInstant(), ZoneId.systemDefault()));
            p.setFechaPrestamo(LocalDateTime.now());
            pdbdao.insert(p);
            l.setEstado("false");
            lidbdao.updateById(l);
        } catch (SQLException ex) {
            Logger.getLogger(PrestamoAgregarFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(PrestamoAgregarFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Guardado con exito. " ,"Exito!", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_btn_guardarActionPerformed

