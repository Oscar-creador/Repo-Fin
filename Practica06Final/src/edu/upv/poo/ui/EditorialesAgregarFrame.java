    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if(accion.equals(ACCION_ALTA))editorial = new Editorial();
        editorial.setNombre(this.txt_nombre.getText());
        editorial.setPais(this.txt_pais.getText());
        editorial.setEmail(this.txt_email.getText());
        editorial.setActivo(true);
        try(Connection connection = App.getConnection()){
            EditorialDbDao edbdao = App.getEditorialDao(connection);
            if(getEditorialGuardadoListener()!=null)
                getEditorialGuardadoListener().editorialGuardado(accion,editorial);
            if (accion.equals(ACCION_ALTA))
                edbdao.insert(editorial);
            if (accion.equals(ACCION_EDITAR))
                edbdao.update(editorial);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(),"Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(),"Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null, "Guardado con exito. " ,"Exito!", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_btn_guardarActionPerformed

